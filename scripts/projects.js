function showLoZ() {
    let projectDcElement = document.getElementById("project_loz");
    if(projectDcElement.style.display === "none") { projectDcElement.style.display = "block"; }
    else { projectDcElement.style.display = "none"; }
}

function showDeClieu() {
    let projectDcElement = document.getElementById("project_dc");
    if(projectDcElement.style.display === "none") { projectDcElement.style.display = "block"; }
    else { projectDcElement.style.display = "none"; }
}